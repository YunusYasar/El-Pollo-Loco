/**
 * Represents the state of keyboard inputs for the game.
 * Tracks whether certain keys are currently pressed.
 */
class Keyboard {
   /**
    * Indicates if the left arrow key is pressed.
    * @type {boolean}
    */
   LEFT = false;

   /**
    * Indicates if the right arrow key is pressed.
    * @type {boolean}
    */
   RIGHT = false;

   /**
    * Indicates if the up arrow key is pressed.
    * @type {boolean}
    */
   UP = false;

   /**
    * Indicates if the down arrow key is pressed.
    * @type {boolean}
    */
   DOWN = false;

   /**
    * Indicates if the space bar is pressed.
    * @type {boolean}
    */
   SPACE = false;

   /**
    * Indicates if the 'D' key is pressed.
    * @type {boolean}
    */
   D = false;
}
